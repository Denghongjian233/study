/**
 * @description: 
 * @param {*} a
 * @param {*} b
 * @return {*}
 */
// 定义一个函数，用于计算两个数的和
function add(a, b) {
    // 返回两个数的和
    return a + b
}

// 使用示例
console.log(add(1, 2)) // 输出：3
/**
 * @description: 
 * @param {*} a
 * @param {*} b
 * @return {*}》
 */
// 函数dec接受两个参数a和b，返回a减去b的值
function dec(a, b) {
    return a - b
}
